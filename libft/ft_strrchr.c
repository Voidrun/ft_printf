#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*res;

	i = 0;
	res = NULL;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			res = (char*)s + i;
		i++;
	}
	if (c == '\0')
		res = (char*)s + i;
	return (res);
}
