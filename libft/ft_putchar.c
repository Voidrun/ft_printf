#include <unistd.h>

void	ft_putchar(int c)
{
	int	left;
	int	right;

	if (c > 127)
	{
		left = 192 + c / 64;
		right = 128 + c % 64;
		write (1, &left, 1);
		write (1, &right, 1);
	}
	else
	{
		write (1, &c, 1);
	}
}
