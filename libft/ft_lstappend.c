#include "libft.h"

void	ft_lstappend(t_list **lst, t_list *new)
{
	t_list	*tmp;

	if (!lst)
		lst = &new;
	if (!(*lst))
		*lst = new;
	else
	{
		tmp = *lst;
		while (tmp->next)
		{
			tmp = tmp->next;
		}
		tmp->next = new;
	}
}
